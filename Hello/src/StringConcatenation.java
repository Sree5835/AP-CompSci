public class StringConcatenation
{  
	public static void main(String[] args)
	{
		System.out.println("Hello my name is Sree.\n\tI am 16 years old.\n 2 plus 4 is 7.");
		System.out.println("Hello my name is Sree.\n\tI am 16 years old.\n" + "2 plus 4 is 7.");
		System.out.println("Hello my name is Sree.\n\tI am 16 years old.\n" + (1+1) +" plus 4 is 7.");
		
		System.out.println();
	}
}
